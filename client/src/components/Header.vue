<template>
  <div>
    <div class="nv-navbar">
      <ul class="nav">
        <!--<li><router-link :to="{ name: 'blogs' }">Blogs</router-link></li>-->
        <li><router-link :to="{ name: 'users' }">Users</router-link></li>
        <li><router-link :to="{ name: 'cars' }">Cars</router-link></li>
        <!-- Show Login if user is not logged in, otherwise show Logout -->
        <li v-if="!isLogin"><router-link :to="{ name: 'login' }">Login</router-link></li>
        <li v-else><a href="#" @click="logout">Logout</a></li>
      </ul>
    </div>
  </div>
</template>

<script>
export default {
  computed: {
    isLogin() {
      return this.$store.state.isUserLoggedIn;
    }
  },
  methods: {
    logout() {
      this.$store.dispatch('setToken', null);
      this.$store.dispatch('setUser', null);

      // Redirect to login page
      this.$router.push({ name: 'login' });
    }
  }
};
</script>

